#include "ft_printf.h"

int	ft_printstr(char *s)
{
	int	len;

	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	len = 0;
	while (*s)
	{
		write(1, s, 1);
		len++;
		s++;
	}
	return (len);
}
