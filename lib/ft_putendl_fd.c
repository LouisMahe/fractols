#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	c;

	if (!s)
		return ;
	c = '\n';
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, &c, 1);
}
